class Solution
{
    public:
    Node *compute(Node *head)
    {
        vector<int>v;
        int i,j,k,l,m,n,p,q;
        p=0;
        while(head)
        {
            v.push_back(head->data);
            head=head->next;
        }
        n=v.size();
        vector<int>u(n,0);
        for(i=n-1;i>=0;i--)
        {
            p=max(p,v[i]);
            u[i]=p;
        }
        for(i=0;i<n;i++)
        {
            if(v[i]>=u[i])
            cout<<v[i]<<" ";
        }
        return head;
    }
    
};
   


