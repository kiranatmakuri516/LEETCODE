class Solution {
  public:
    int equalSum(int n, vector<int> a) {
        int i,j,k,l,m,p,q,r,s,t,u,v,x,y,z;
        p=0;
        for(i=0;i<n;i++)
        p+=a[i];
        q=0;
        for(i=0;i<n;i++)
        {
            if(p-a[i]-q==q) return i+1;
            q+=a[i];
        }
        return -1;
    }
};


